public class Lesson_33_Activity_Three {

    // Create method printit() with an integer array parameter
    public static void printit(int[] array)
    {
        // Iterate through the array
        for(int i = 0; i < array.length; i++)
        {
            // Print each element with a space after
            System.out.print(array[i] + " ");
        }
    }
}
